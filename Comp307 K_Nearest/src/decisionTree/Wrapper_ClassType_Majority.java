package decisionTree;

public class Wrapper_ClassType_Majority {

	private String classType;
	private float majority;
	public Wrapper_ClassType_Majority(String classType, float majority) {
		super();
		this.classType = classType;
		this.majority = majority;
	}
	public String getClassType() {
		return classType;
	}
	public float getMajority() {
		return majority;
	}
	
	
}
